// 字库大小
const uint32_t font_database_size = sizeof(font_database);

// 字符数量
const uint8_t font_char_count = font_database_size / FONT_CHAR_SIZE;

/**
 * @brief 将字库存储到Flash
 * @retval 无
 */
void Font_StoreToFlash(void)
{
    uint32_t sector_address = FONT_START_ADDR;
    uint32_t sectors_needed = (font_database_size + 4095) / 4096; // 向上取整

    // 擦除存储区域
    for (uint32_t i = 0; i < sectors_needed; i++)
    {
        Flash_Delete(sector_address + i * 4096);
    }

    // 分批写入数据（每次最多256字节）
    uint32_t offset = 0;
    while (offset < font_database_size)
    {
        uint8_t len = (font_database_size - offset) > 255 ? 255 : (font_database_size - offset);
        Flash_WriteData((uint8_t *)&font_database[offset], len, FONT_START_ADDR + offset);
        offset += len;
    }

    printf("Font database stored to Flash successfully!\r\n");
    printf("Total size: %lu bytes\r\n", font_database_size);
    printf("Total characters: %u\r\n", font_char_count);
}

/**
 * @brief 从Flash读取指定字符的字模
 * @param index: 字符索引
 * @param data: 存储字模数据的缓冲区（至少32字节）
 * @retval 无
 */
void Font_ReadChar(uint8_t index, uint8_t *data)
{
    if (index >= font_char_count)
        return;

    // 计算字符在Flash中的地址
    uint32_t address = FONT_START_ADDR + index * FONT_CHAR_SIZE;

    // 从Flash读取数据
    Flash_ReadData(data, FONT_CHAR_SIZE, address);
}

/**
 * @brief 显示指定字符
 * @param x, y: 显示位置
 * @param index: 字符索引
 * @retval 无
 */
void Font_DisplayChar(uint16_t x, uint16_t y, uint8_t index)
{
    uint8_t char_data[FONT_CHAR_SIZE];

    // 读取字符字模
    Font_ReadChar(index, char_data);

    // 显示字符
    LCD_ShowChinese(x, y, 16, char_data);
}

/**
 * @brief 获取字符数量
 * @retval 字符数量
 */
uint8_t Font_GetCharCount(void)
{
    return font_char_count;
}
